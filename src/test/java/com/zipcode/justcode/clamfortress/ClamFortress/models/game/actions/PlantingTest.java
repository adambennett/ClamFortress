package com.zipcode.justcode.clamfortress.ClamFortress.models.game.actions;


import com.zipcode.justcode.clamfortress.ClamFortress.models.game.actions.priority.*;
import com.zipcode.justcode.clamfortress.ClamFortress.models.game.models.*;
import com.zipcode.justcode.clamfortress.ClamFortress.models.game.models.resources.natural.*;
import org.junit.jupiter.api.*;

public class PlantingTest {

    @Test
    public void hasSeedTest(){
        Game.startGame();
        Seeds seeds = new Seeds();
        Planting planting = new Planting(1);
        Game.getVillage().addResource(seeds,10);

        Boolean expected = true;
        Boolean actual = planting.hasSeeds();

        Assertions.assertEquals(expected,actual);
    }

    @Test
    public void populateSeedsTest(){
        Game.startGame();
        Seeds seeds = new Seeds();
        Planting planting = new Planting(1);
        Game.getVillage().addResource(seeds, 10);

       Integer actual =  planting.populateSeeds();
       Integer expected = 10;

       Assertions.assertEquals(expected,actual);
    }

    @Test
    public void updateTest(){
        Game.startGame();
        Seeds seeds = new Seeds();
        Planting planting = new Planting(1);
        Game.getVillage().addResource(seeds, 10);
        planting.update();

        Integer expectedSeeds = 0;//0
        Integer actualSeeds = planting.populateSeeds();

        Integer seedsNotReadyToPopulateexpected = 10;//10

        Integer seedsNotReadyToPopulateActual = Game.getVillage().getFarmland().getNotReadyToHarvestSeeds().size();

        Assertions.assertEquals(seedsNotReadyToPopulateexpected,seedsNotReadyToPopulateActual);
        Assertions.assertEquals(expectedSeeds,actualSeeds);
    }
}
